const fs = require('fs');
const path = require('path');

/**
 * Utility script to convert font files to Base64 and generate a TypeScript font-face injection file.
 * This ensures Puppeteer can render documents with consistent typography in offline environments.
 */

const FONT_DIR = process.argv[2] || path.join(__dirname, '../assets/fonts'); 
const OUTPUT_FILE = path.join(__dirname, '../utils/embeddedFonts.ts');

function generateEmbeddedFonts() {
  if (!fs.existsSync(FONT_DIR)) {
    console.error(`Font directory not found: ${FONT_DIR}`);
    console.log('Please create the directory and add your .woff2 or .ttf files.');
    return;
  }

  const files = fs.readdirSync(FONT_DIR);
  const fontFaceDeclarations = [];

  files.forEach(file => {
    const ext = path.extname(file).toLowerCase();
    if (ext === '.woff2' || ext === '.woff' || ext === '.ttf') {
      const fontName = path.basename(file, ext);
      const filePath = path.join(FONT_DIR, file);
      const fontData = fs.readFileSync(filePath);
      const base64Data = fontData.toString('base64');
      
      let format = 'truetype';
      if (ext === '.woff2') format = 'woff2';
      if (ext === '.woff') format = 'woff';

      // Determine weight and style from filename
      let weight = 'normal';
      let style = 'normal';
      
      if (fontName.toLowerCase().includes('bold')) weight = 'bold';
      if (fontName.toLowerCase().includes('italic')) style = 'italic';
      if (fontName.toLowerCase().includes('light')) weight = '300';
      if (fontName.toLowerCase().includes('black')) weight = '900';
      if (fontName.toLowerCase().includes('medium')) weight = '500';

      const familyName = fontName.split('-')[0] || 'CustomFont';

      fontFaceDeclarations.push(`
@font-face {
  font-family: '${familyName}';
  src: url(data:font/${format};charset=utf-8;base64,${base64Data}) format('${format}');
  font-weight: ${weight};
  font-style: ${style};
  font-display: swap;
}`);
    }
  });

  const content = `/**
 * AUTO-GENERATED FILE - DO NOT EDIT DIRECTLY
 * Generated by scripts/generate-font-base64.cjs
 * This file contains Base64 encoded fonts for offline PDF rendering.
 */

export const EMBEDDED_FONTS_CSS = \`${fontFaceDeclarations.join('\n')}\`;
`;

  fs.writeFileSync(OUTPUT_FILE, content);
  console.log(`Successfully generated ${OUTPUT_FILE} with ${fontFaceDeclarations.length} fonts.`);
}

generateEmbeddedFonts();
